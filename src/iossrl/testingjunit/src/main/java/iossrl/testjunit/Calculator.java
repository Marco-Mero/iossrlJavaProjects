package iossrl.testjunit;

/**
 * Textbook Caclulator class for JUnit.
 */
public class Calculator {
    /**
     * Adds the two addendums.
     * @param add1 first addendum.
     * @param add2 second addendum.
     * @return double sum.
     */
    public double add(double add1, double add2) {
        return add1 + add2;
    }
}
